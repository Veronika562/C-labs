#include "Calculations.h"
#include <assert.h>

void test_calc_eps(double x, int n, double eps) {
	assert(calc_eps(x, n) == eps);
}

void test_calc_n(double x, int n, double eps) {
	assert(calc_n(x, eps) == n);
}

#undef main

int main() {
	test_calc_n(2.0, 9, 0.00000000003232909000);
	test_calc_n(5.003, 221, 0.000000000000000052500);
	test_calc_n(3.589200, 14, 0.00000000000008288800);
	test_calc_eps(2.0, 9, 0.0000000000042692516188935769605450332164764404296875000000000000000000000000000000000000000000000000);
	test_calc_eps(5.00, 15, 0.0000000000005559996907322783954441547393798828125000000000000000000000000000000000000000000000000000);
	test_calc_eps(3.5, 11, 0.0000000001236711844043725250230636447668075561523437500000000000000000000000000000000000000000000000);
	return 0;
}
